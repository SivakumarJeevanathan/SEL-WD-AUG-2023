package week1.day2;

public class LearnOperator 
{
	public static void main(String[] args) 
	{
		int Base_Number=10;  
		System.out.print("List of odd numbers from 1 to "+Base_Number+": ");  
		for (int i=1; i<=Base_Number; i++)   
		{  
		if (i%2!=0)   
			{  
				System.out.print(i + " ");  
			}  
		}
	}
}
